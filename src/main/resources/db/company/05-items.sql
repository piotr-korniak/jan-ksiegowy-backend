CREATE TABLE ITEMS (
       ID BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
       ITEM_ID UUID,
       DATE DATE NOT NULL,
       TYPE CHAR(1),
       CODE VARCHAR(32),
       NAME VARCHAR(128),
       TAX_RATE CHAR(2),
       TAX_METOD CHAR(2),
       SOLD BOOLEAN,
       PURCHASED BOOLEAN,
       MEASURE VARCHAR(10),

       PRIMARY KEY (ID),
       UNIQUE (ITEM_ID, DATE)
);