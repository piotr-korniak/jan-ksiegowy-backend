CREATE TABLE METRICS (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    DATE DATE NOT NULL,
    TAX_NUMBER VARCHAR(13),
    REGISTRATION_NUMBER VARCHAR(32),
    NAME VARCHAR(128),
    ADDRESS VARCHAR(64),
    TOWN VARCHAR(32),
    POSTCODE VARCHAR(7),
    COUNTRY VARCHAR(2),
    CAPITAL DECIMAL(12,2) NOT NULL,
    VAT_QUARTERLY BOOLEAN,
    CIT_QUARTERLY BOOLEAN,
    US_ID BIGINT,
    VAT_UE BOOLEAN,

    PRIMARY KEY (ID)
);
