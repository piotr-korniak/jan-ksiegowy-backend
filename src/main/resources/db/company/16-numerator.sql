CREATE TABLE NUMERATORS (
	ID BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
	CODE VARCHAR(32),
	NAME VARCHAR(128),
	MASK VARCHAR(32),
	TYPE VARCHAR(1) NOT NULL,

	PRIMARY KEY (ID)
);

CREATE TABLE COUNTERS (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    NUMERATOR_ID BIGINT,
    MONTH INTEGER,
    YEARS INTEGER,
    TYPE VARCHAR(9),
    VALUES BIGINT,
    VERSION BIGINT,

    PRIMARY KEY (ID)
);