CREATE TABLE TEMPLATES (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    TEMPLATE_ID UUID,
    CODE VARCHAR(24),
    DATE DATE NOT NULL,
    DOCUMENT_TYPE VARCHAR(2),
    ENTITY_TYPE CHAR(1),

    REGISTER_ID UUID,
    NAME VARCHAR(128),

    PRIMARY KEY (ID)
);