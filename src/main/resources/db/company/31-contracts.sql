    GROSS DECIMAL(12,2) NOT NULL,
    INSURANCE_EMPLOYEE DECIMAL(12,2) NOT NULL,
    INSURANCE_EMPLOYER DECIMAL(12,2) NOT NULL,
    INSURANCE_HEALTH DECIMAL(12,2) NOT NULL,
    TAX_ADVANCE DECIMAL(12,2) NOT NULL,

    PRIMARY KEY (ID)
);
